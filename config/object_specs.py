
def createBrick(elevation = 30,
                heading = 0,
                speed = 0,
                stopHeading = -1,
                lives = 1,
                value = 1,
                modifier = None,
                color = None,
                size = None):
  return dict(_type = "Brick",
              elevation = elevation,
              heading = heading,
              size = size,
              value = value,
              lives = lives,
              color = color,
              speed = speed,
              stopHeading = stopHeading,
              modifier = modifier)

